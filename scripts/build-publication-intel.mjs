/**
 * Build the canonical per-publication editorial intelligence dataset from the
 * source "Publications That Pay" Google Sheet tabs (exported as CSV under
 * /tmp/pubintel). Merges every tab by normalized publication name, preferring
 * the longest non-empty value per field, and emits a typed TS module.
 *
 * Re-run:
 *   for gid in 103885064 1171839630 1430571046 1999591902 1942661265 1469318569; do
 *     curl -sL "https://docs.google.com/spreadsheets/d/<SHEET_ID>/export?format=csv&gid=$gid" -o /tmp/pubintel/$gid.csv
 *   done
 *   node scripts/build-publication-intel.mjs
 */
import fs from "node:fs";
import path from "node:path";

const SRC_DIR = "/tmp/pubintel";
const OUT = path.resolve("shared/publication-intelligence.ts");

// Canonical field <- possible source header names.
const FIELD_MAP = {
  writingStyle: ["Enhanced Writing Style", "Writing Style to Use"],
  targetAudience: ["Target Audience"],
  editorStyle: ["Editor's Style"],
  topics: ["Topics"],
  editorLikes: ["What This Editor Likes"],
  businessTopics: ["Business Topics"],
  keywordFilter: ["Keyword Filter"],
  newsSources: ["News Sources"],
  scoringAlgorithm: ["Scoring Algorithm"],
};
const LIST_FIELDS = new Set(["businessTopics", "keywordFilter", "newsSources"]);

function parseCsv(text) {
  const rows = [];
  let row = [], field = "", inQuotes = false;
  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    if (inQuotes) {
      if (c === '"') {
        if (text[i + 1] === '"') { field += '"'; i++; }
        else inQuotes = false;
      } else field += c;
    } else if (c === '"') inQuotes = true;
    else if (c === ",") { row.push(field); field = ""; }
    else if (c === "\n") { row.push(field); rows.push(row); row = []; field = ""; }
    else if (c === "\r") { /* skip */ }
    else field += c;
  }
  if (field.length || row.length) { row.push(field); rows.push(row); }
  return rows;
}

function slugify(name) {
  return name.toLowerCase().trim()
    .replace(/&/g, " and ")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

const merged = new Map();

for (const file of fs.readdirSync(SRC_DIR).filter(f => f.endsWith(".csv"))) {
  const rows = parseCsv(fs.readFileSync(path.join(SRC_DIR, file), "utf8"));
  if (!rows.length) continue;
  const header = rows[0].map(h => h.trim());
  const colIndex = (names) => names.map(n => header.indexOf(n)).find(i => i >= 0) ?? -1;
  const pubCol = header.indexOf("Publication");
  if (pubCol < 0) continue;
  const resolved = {};
  for (const [field, names] of Object.entries(FIELD_MAP)) resolved[field] = colIndex(names);

  for (const r of rows.slice(1)) {
    const name = (r[pubCol] || "").trim();
    if (!name) continue;
    const slug = slugify(name);
    if (!slug) continue;
    const entry = merged.get(slug) || { slug, name };
    for (const [field, idx] of Object.entries(resolved)) {
      if (idx < 0) continue;
      const val = (r[idx] || "").trim();
      if (!val) continue;
      // Prefer the longest non-empty value across duplicate tabs.
      if (!entry[field] || val.length > String(entry[field]).length) entry[field] = val;
    }
    merged.set(slug, entry);
  }
}

// Finalize: split list fields into string[].
const records = [...merged.values()].map(e => {
  const out = { slug: e.slug, name: e.name };
  for (const field of Object.keys(FIELD_MAP)) {
    const v = e[field];
    if (!v) continue;
    if (LIST_FIELDS.has(field)) {
      const list = v.split(/[,;\n]/).map(s => s.trim().toLowerCase()).filter(Boolean);
      const uniq = [...new Set(list)];
      if (uniq.length) out[field] = uniq;
    } else {
      out[field] = v.replace(/\s+/g, " ").trim();
    }
  }
  return out;
}).sort((a, b) => a.name.localeCompare(b.name));

const banner = `// AUTO-GENERATED by scripts/build-publication-intel.mjs — do not edit by hand.
// Canonical per-publication editorial intelligence sourced from the
// "Publications That Pay" sheet: writing style, target audience, editor's style,
// what the editor likes, business topics, keyword filter, news sources, and the
// per-publication scoring algorithm. Consumed by scoring, matching & research.
`;

const out = `${banner}
export interface PublicationIntel {
  slug: string;
  name: string;
  writingStyle?: string;
  targetAudience?: string;
  editorStyle?: string;
  topics?: string;
  editorLikes?: string;
  businessTopics?: string[];
  keywordFilter?: string[];
  newsSources?: string[];
  scoringAlgorithm?: string;
}

export const PUBLICATION_INTEL: PublicationIntel[] = ${JSON.stringify(records, null, 2)};

function normalizeKey(s: string): string {
  return s.toLowerCase().trim().replace(/&/g, " and ").replace(/[^a-z0-9]+/g, "-").replace(/^-+|-+$/g, "");
}

const BY_SLUG = new Map<string, PublicationIntel>(PUBLICATION_INTEL.map(p => [p.slug, p]));

/** Resolve publication intelligence by slug or display name (fuzzy-tolerant). */
export function getPublicationIntel(nameOrSlug?: string | null): PublicationIntel | null {
  if (!nameOrSlug) return null;
  const key = normalizeKey(nameOrSlug);
  if (BY_SLUG.has(key)) return BY_SLUG.get(key)!;
  // Fallback: prefix / contains match (handles "Forbes" vs "forbes-com" etc.)
  const partial = PUBLICATION_INTEL.find(p => p.slug === key || p.slug.startsWith(key) || key.startsWith(p.slug));
  return partial ?? null;
}

/** Flattened, de-duplicated keyword set (keyword filter + business topics). */
export function intelKeywords(intel: PublicationIntel): string[] {
  return [...new Set([...(intel.keywordFilter ?? []), ...(intel.businessTopics ?? [])])];
}

export const PUBLICATION_INTEL_COUNT = PUBLICATION_INTEL.length;
`;

fs.writeFileSync(OUT, out);
const withScoring = records.filter(r => r.scoringAlgorithm).length;
const withKeywords = records.filter(r => r.keywordFilter).length;
const withNews = records.filter(r => r.newsSources).length;
console.log(`Wrote ${OUT}`);
console.log(`  publications: ${records.length}`);
console.log(`  with scoringAlgorithm: ${withScoring}`);
console.log(`  with keywordFilter:    ${withKeywords}`);
console.log(`  with newsSources:      ${withNews}`);
console.log(`  sample: ${records.slice(0, 5).map(r => r.name).join(", ")}`);
